print("\nComparison")
print(a == b)
print(a != b)
print(a > b)
print(a < b)
print(a >= b)
print(a <= b)

print("\nLogical")
p = True
q = False
print(p and q)
print(p or q)
print(not p)
print(not q)

print("\nBitwise")
m = 10
n = 4
print(m & n)
print(m | n)
print(m ^ n)
print(~m)
print(m << 2)
print(m >> 2)

print("\nIdentity")
l1 = [10, 20]
l2 = [10, 20]
l3 = l1
print(l1 is l2)
print(l1 is l3)
print(l1 is not l2)
print(l1 is not l3)

print("\nMembership")
num = [10, 20, 30, 40]
print(20 in num)
print(100 in num)
print(50 not in num)
